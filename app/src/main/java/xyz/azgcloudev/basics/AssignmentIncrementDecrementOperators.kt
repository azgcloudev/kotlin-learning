package xyz.azgcloudev.basics

fun main() {

    // Assignment Operators  [ += -= *= /= %= ]
    var number1 = 5
    number1 += 4
    println(number1) //result is: 9


    // Increment and decrement [ ++, -- ]
    // increase the value by one or decrease it by 1
    println(number1) // 9
    println(number1++) // 9; sum is execute at the end of the line
    println(++number1) //11 // sum is execute in the same line

}